/*
 * N 찍기
 * https://www.acmicpc.net/problem/2741
 */

package boj;

import java.util.Scanner;

public class boj2741 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		for (int i = 1; i <= N; i++) {
			System.out.println(i);
		}
	}
}
